void Oled::intro(Adafruit_SSD1306 display)
{
    int LOGO_HEIGHT = 60;
    int LOGO_WIDTH = 60;

    display.clearDisplay();
    display.drawBitmap(
        (display.width() - LOGO_WIDTH) / 2,
        (display.height() - LOGO_HEIGHT) / 2,
        logo, LOGO_WIDTH, LOGO_HEIGHT, 1);
    display.display();
    delay(2000);

    display.clearDisplay();
    display.display();
    delay(1000);
}

void Oled::writeModeInfo(String text, Adafruit_SSD1306 display)
{
    String finalText = "Tryb: " + text;
    int strLen = finalText.length() + 1; 
    char charArray[strLen];
    
    finalText.toCharArray(charArray, strLen);

    display.setTextSize(1.5);
    display.setTextColor(SSD1306_WHITE);
    display.setCursor(0, 0);
    display.print(charArray);
    display.display();
    // delay(100);
}

void Oled::writeStateInfo(String text, Adafruit_SSD1306 display)
{
    String finalText = "Stan: " + text;
    int strLen = finalText.length() + 1; 
    char charArray[strLen];
    
    finalText.toCharArray(charArray, strLen);
    
    display.setTextSize(1.5);
    display.setTextColor(WHITE, BLACK);
    display.setCursor(0, 0);
    display.print(charArray);
    display.display();
    // delay(100);
}